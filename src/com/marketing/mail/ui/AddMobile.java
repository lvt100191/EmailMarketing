    private Mobile initMobile(String number) {
        Mobile m = new Mobile();
        m.setMobile(number);
        Date d = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyyMMddHHmmss");
        String dateCreate = sdf.format(d);
        m.setCreateDate(dateCreate);
        return m;
    }
