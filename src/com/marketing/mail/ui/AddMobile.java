    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String mobile = txtNumBer.getText().trim();
        txtRs.setText("");
        String rs = "";
        String numExisted = "";
        if (mobile == null || mobile.isEmpty()) {
            rs = rs + "Yêu cầu nhập [Số điện thoại] " + "\n";
        }
        if (rs.isEmpty()) {
            String[] lstMobile = mobile.trim().split("\n");
            for (String number : lstMobile) {
                try {
                    Mobile m = MobileDao.getMobile(number.trim());
                    if (m != null) {
                        numExisted = numExisted + number + "\n";
                    } else {
                        Mobile mobileInsert = initMobile(number);
                        MobileDao.insert(mobileInsert);
                        rs = rs + number + "\n";
                    }
                } catch (Exception ex) {
                    Logger.getLogger(AddMobile.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        txtRs.setText("Danh sach so dien thoai them moi: "+rs
                     +"\n Danh sach so dien thoai da ton tai:"+numExisted
        );
    }//GEN-LAST:event_jButton1ActionPerformed

